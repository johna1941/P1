/// \file P1ActionInitialization.cc
/// \brief Implementation of the P1ActionInitialization class

#include "P1ActionInitialization.hh"
#include "P1PrimaryGeneratorAction.hh"
#include "P1RunAction.hh"
#include "P1EventAction.hh"
#include "P1SteppingAction.hh"

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

P1ActionInitialization::P1ActionInitialization()
 : G4VUserActionInitialization()
{}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

P1ActionInitialization::~P1ActionInitialization()
{}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void P1ActionInitialization::BuildForMaster() const
{
  SetUserAction(new P1RunAction);
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void P1ActionInitialization::Build() const
{
  SetUserAction(new P1PrimaryGeneratorAction);
  SetUserAction(new P1RunAction);
  
  P1EventAction* eventAction = new P1EventAction;
  SetUserAction(eventAction);
  
  SetUserAction(new P1SteppingAction(eventAction));
}  

